    private void excluirTecnicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirTecnicoActionPerformed
        // TODO add your handling code here:
        if (DadosTecnicos.buscar(cpfTec.getText()) == null  )
            JOptionPane.showMessageDialog(null,"Técnico não encontrado!");
        else{
            if (DadosTecnicos.excluir(cpfTec.getText()))
                JOptionPane.showMessageDialog(null,"Técnico excluído com sucesso!");
                endTec.setText("");
                nomeTec.setText("");
                endTec.setText("");
                telTec.setText("");
                carTec.setText("");
                depTec.setText("");
                valHoraExtraTec.setText("");
                qtdHoraExtraTec.setText("");                
        }
    }//GEN-LAST:event_excluirTecnicoActionPerformed

