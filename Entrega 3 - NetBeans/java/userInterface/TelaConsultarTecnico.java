    private void consultarTecnicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarTecnicoActionPerformed
        // TODO add your handling code here:        
        Tecnico tecnico;
        tecnico = DadosTecnicos.buscar(cpfTec.getText());
        if (tecnico == null)
            JOptionPane.showMessageDialog(null,"Técnico não encontrado!");
        else{
            nomeTec.setText(tecnico.getNome());
            endTec.setText(tecnico.getEndereco());
            telTec.setText(tecnico.getTelefone());
            carTec.setText(tecnico.getCargo());
            depTec.setText(tecnico.getDepartamento());
            ctpsTec.setText(tecnico.getCtps());
            valHoraExtraTec.setText(String.valueOf(tecnico.getQuantidadeHoraExtra()));
            qtdHoraExtraTec.setText(String.valueOf(tecnico.getValorHoraExtra()));
        }
        
    }//GEN-LAST:event_consultarTecnicoActionPerformed

